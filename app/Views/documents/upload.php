                    e.preventDefault();
                    const tabId = this.getAttribute('data-tab');
                    switchToTab(tabId);
                });
            });

            // Handle direct links with hash
            if (window.location.hash) {
                const hash = window.location.hash.substring(1);
                if (hash === 'cpns-tab' || hash === 'pppk-tab') {
                    switchToTab(hash);
                }
            }

            // Handle URL parameters
            const urlParams = new URLSearchParams(window.location.search);
            const tabParam = urlParams.get('tab');
            if (tabParam === 'cpns' || tabParam === 'pppk') {
                switchToTab(tabParam + '-tab');
            }
        });

        // JavaScript to load the FAQ content dynamically
        document.addEventListener('DOMContentLoaded', function() {
            fetch('assets/pages/faq.html')
                .then(response => response.text())
                .then(data => {
                    document.getElementById('faq-container').innerHTML = data;
                })
                .catch(error => console.error('Error loading FAQ:', error));
        });
    </script>

